$(document).ready( function () {


    $("#login_items").mouseover(function() {
        $("#login_box").fadeIn();
    });

     $("#login_items").mouseleave(function() {
        $("#login_box").fadeOut();
    });

     $("#register_items").mouseover(function() {
        $("#register_box").fadeIn();
    });

     $("#register_items").mouseleave(function() {
        $("#register_box").fadeOut();
    });

});
